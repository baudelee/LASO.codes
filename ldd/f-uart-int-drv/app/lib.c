
int atoi(char * buf)
{
	int value = 0;	
	int base = 10;
	int i = 0;

	if (buf[0] == '0' && buf[1] == 'x')
	{
		base = 16;
		i = 2;
	}

	// 123 = (1 * 10 + 2) * 10 + 3
	// 0x1F = 1 * 16 + F(15)
	while (buf[i])
	{
		int tmp;

		if (buf[i] <= '9' && buf[i] >= '0')
			tmp = buf[i] - '0';
		else
			tmp = buf[i] - 'a' + 10;

		value = value * base + tmp;

		i++;
	}

	return value;
}

int strcmp(const char * s1, const char * s2)
{
	while (*s1 == *s2)
	{
		if (*s1 == '\0')
			return 0;	
		s1++;
		s2++;	
	}

	return *s1 - *s2;
}
